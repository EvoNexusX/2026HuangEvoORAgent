"""
数学建模准确率对比可视化工具
==============================
本脚本用于比较使用OR-LLM-Agent（带数学建模）和直接推理（不带数学建模）
两种方法在不同LLM模型上的性能表现。

生成分组条形图，展示两种方法的准确率对比，
帮助评估数学建模步骤对求解精度的影响。
"""

# ============================================================================
# 导入依赖库
# ============================================================================
import matplotlib.pyplot as plt  # 绘图库
import numpy as np  # 数值计算库

# ============================================================================
# 数据定义
# ============================================================================

# 模型名称列表
models = [
    "", 
    "", 
    "", 
    "", 
    "", 
    ""
]

# 两种方法的准确率数据（%）
math_error_agent = [80.25, 80.49, 82.93, 53.75, 65.85, 69.51]  # OR-LLM-Agent（带数学建模）
math_error_no_agent = [78.48, 73.75, 76.92, 44.59, 52.56, 71.83]  # 直接推理（不带数学建模）

# ============================================================================
# 图表样式配置
# ============================================================================

# x轴位置设置
x = np.arange(len(models))  # 模型数量对应的x坐标数组
width = 0.35  # 每个条形的宽度

# 设置全局字体为Times New Roman（学术论文标准）
plt.rcParams["font.family"] = "Times New Roman"

# ============================================================================
# 绘制图表
# ============================================================================

# 创建图表
fig, ax = plt.subplots(figsize=(12, 6))

# 绘制第一组条形（OR-LLM-Agent）
bars1 = ax.bar(
    x - width/2,  # x坐标位置（向左偏移半个宽度）
    math_error_agent,  # 数据
    width,  # 条形宽度
    label='OR-LLM-Agent',  # 图例标签
    hatch='//',  # 填充图案：斜线
    color='lightgray',  # 填充颜色：浅灰色
    edgecolor='white'  # 边框颜色：白色
)

# 绘制第二组条形（Reasoning and Non-Reasoning LLMs）
bars2 = ax.bar(
    x + width/2,  # x坐标位置（向右偏移半个宽度）
    math_error_no_agent,  # 数据
    width,  # 条形宽度
    label='Reasoning and Non-Reasoning LLMs',  # 图例标签
    hatch='\\\\',  # 填充图案：反斜线
    color='lightblue',  # 填充颜色：浅蓝色
    edgecolor='white'  # 边框颜色：白色
)

# ============================================================================
# 添加数值标签
# ============================================================================

# 在每个条形顶部添加数值标签
for bar in bars1 + bars2:
    height = bar.get_height()  # 获取条形高度（数值）
    # 在条形顶部上方添加文字标注
    ax.annotate(
        f'{height:.2f}',  # 显示文字（保留两位小数）
        xy=(bar.get_x() + bar.get_width() / 2, height),  # 标注位置（条形中心，顶部）
        xytext=(0, 3),  # 文字偏移（向上3个点）
        textcoords="offset points",  # 偏移单位：点
        ha='center',  # 水平对齐：居中
        va='bottom',  # 垂直对齐：底部对齐
        fontsize=14  # 字体大小
    )

# ============================================================================
# 坐标轴和网格设置
# ============================================================================

# 设置y轴标签
ax.set_ylabel('Scores (%)', fontsize=16)
ax.tick_params(axis='y', labelsize=16)

# 设置x轴刻度和标签
ax.set_xticks(x)  # x轴刻度位置
ax.set_xticklabels(models, fontsize=18)  # x轴标签（模型名称）

# 设置y轴范围
ax.set_ylim(0, 100)  # 0-100%

# 添加y轴网格线（虚线，半透明）
ax.grid(axis='y', linestyle='--', alpha=0.5)

# 移除顶部和右侧边框线（更简洁的外观）
ax.spines['top'].set_visible(False)
ax.spines['right'].set_visible(False)

# ============================================================================
# 图例设置
# ============================================================================

# 图例置顶（位于图表上方，水平排列）
ax.legend(
    loc='upper center',  # 位置：上方居中
    bbox_to_anchor=(0.5, 1.1),  # 具体位置微调
    ncol=2,  # 2列布局
    fontsize=16,  # 字体大小
    frameon=False  # 无边框
)

# ============================================================================
# 保存和显示
# ============================================================================

plt.tight_layout()  # 自动调整布局，避免标签被裁剪
plt.savefig("data/images/bar_math_accurary.png", dpi=300, bbox_inches='tight')  # 保存高清图片
plt.show()  # 显示图表

